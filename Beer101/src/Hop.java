
public class Hop {
	public int idHop;
	public String HopName;
	public double HopQuanty;
	
	public int getIdHop() {
		return idHop;
	}
	public void setIdHop(int idHop) {
		this.idHop = idHop;
	}
	public String getHopName() {
		return HopName;
	}
	public void setHopName(String hopName) {
		HopName = hopName;
	}
	public double getHopQuanty() {
		return HopQuanty;
	}
	public void setHopQuanty(double hopQuanty) {
		HopQuanty = hopQuanty;
	}
}
