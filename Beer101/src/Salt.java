
public class Salt {
	public int idSalt;
	public String SaltName;
	public double SaltQuanty;
	
	public int getIdSalt() {
		return idSalt;
	}
	public void setIdSalt(int idSalt) {
		this.idSalt = idSalt;
	}
	public String getSaltName() {
		return SaltName;
	}
	public void setSaltName(String saltName) {
		SaltName = saltName;
	}
	public double getSaltQuanty() {
		return SaltQuanty;
	}
	public void setSaltQuanty(double saltQuanty) {
		SaltQuanty = saltQuanty;
	}
}
